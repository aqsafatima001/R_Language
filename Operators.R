# Operators in R programming Language

# Arthematic --->  + - * / %%(Remainder) %/%(Quotient) ^(Power)
# Logical    --->  & | ! && ||
# Relational ---> < > == <= >= !=
# Assignment --->


#Arthematic Operators Example:
a<-7.5
b<-4

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b) #Remainder
print(a%/%b) #Quotient
print(a^b) #Power


c1<-c(1,2,3)
c2<-c(4,5,6)

print(c1+c2)
print(c1-c2)
print(c1*c2)
print(c1/c2)
print(c1%%c2) #Reaminder
print(c1%/%c2) #Quotient
print(c1^c2) #Power

